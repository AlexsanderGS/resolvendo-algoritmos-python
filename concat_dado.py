# Você recebeu dois dados diferentes do usário e deve concatena-los em uma única string.

# Recebe o primeiro dado do usuário
dado1 = input("Digite o primeiro dado: ")

# Recebe o segundo dado do usuário
dado2 = input("Digite o segundo dado: ")

# Concatena os dados
resultado = dado1 + " " + dado2

# Mostra o resultado
print("A string concatenada é:", resultado)
